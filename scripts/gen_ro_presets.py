#!/usr/bin/env python3
"""Emit hardcoded RO bank tune presets + PBLOCK regions from characterization targets."""

from __future__ import annotations

from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]

SDUP_SV_HEADER = """// =============================================================================
// Projekt SDUP — aring_osc
// A. Kowalczyk, K. Skalka
// Ring Oscillator Synthesizer — Arty S7-50 (V1 UART)
// =============================================================================

"""

# bank HW id, tune12, nominal Hz, PBLOCK slice rect (xc7s50) — no overlap
