    {"bank": 10, "tune": 0x000, "f_hz": 120_000_000, "rect": "SLICE_X45Y45:SLICE_X55Y55"},
    {"bank": 0,  "tune": 0x000, "f_hz": 100_000_000, "rect": "SLICE_X56Y45:SLICE_X59Y55"},
    {"bank": 3,  "tune": 0x060, "f_hz": 55_000_000,  "rect": "SLICE_X0Y45:SLICE_X12Y58"},
